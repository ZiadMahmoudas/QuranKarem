// Completion tracking
let progress = JSON.parse(localStorage.getItem('athkar_progress')||'{}');

function saveProgress(){localStorage.setItem('athkar_progress',JSON.stringify(progress));}

function renderCards(catId){
  const items = DATA[catId];
  const container = document.getElementById('cards-'+catId);
  if(!container||container.children.length>0)return;
  items.forEach((item,i)=>{
    const key = catId+'_'+i;
    const isDone = progress[key]&&progress[key]>=(item.count);
    const card = document.createElement('div');
    card.className = 'dhikr-card';
    card.innerHTML = `
      <button class="copy-btn" title="نسخ" onclick="copyText(this,'${encodeURIComponent(item.text)}')">⧉</button>
      <div class="dhikr-arabic">${item.text}</div>
      <div class="dhikr-source">
        <span class="dhikr-source-text">${item.source}</span>
        <span class="dhikr-count-badge ${isDone?'done':''}" id="badge-${key}" onclick="tickCount('${key}',${item.count},this)">
          ${isDone?'✓ اكتمل':`${progress[key]||0} / ${item.count} مرة`}
        </span>
      </div>`;
    container.appendChild(card);
  });
}

function tickCount(key,max,el){
  if(!progress[key])progress[key]=0;
  if(progress[key]>=max){progress[key]=0;el.classList.remove('done');el.textContent='0 / '+max+' مرة';}
  else{
    progress[key]++;
    if(progress[key]>=max){el.classList.add('done');el.textContent='✓ اكتمل';}
    else{el.textContent=progress[key]+' / '+max+' مرة';}
  }
  saveProgress();
}

function copyText(btn,encoded){
  navigator.clipboard.writeText(decodeURIComponent(encoded)).then(()=>{
    btn.textContent='✓';btn.classList.add('copied');
    setTimeout(()=>{btn.textContent='⧉';btn.classList.remove('copied');},1500);
  });
}

// Tabs
document.querySelectorAll('.tab').forEach(tab=>{
  tab.addEventListener('click',()=>{
    document.querySelectorAll('.tab').forEach(t=>t.classList.remove('active'));
    document.querySelectorAll('.section').forEach(s=>s.classList.remove('active'));
    tab.classList.add('active');
    const id = tab.dataset.tab;
    document.getElementById('sec-'+id).classList.add('active');
    renderCards(id);
    window.scrollTo({top:0,behavior:'smooth'});
  });
});

